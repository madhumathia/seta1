#include<stdio.h>
void main()
{
    int number;
    scanf("%d",&number);
    printf(" The given number is : %d",number);
    if(number>0)
    {
        printf("\npositive");
    }
    else if(number<0)
    {
        printf("\n negative");
    }
    else if(number==0)
    {
        printf("\nzero");
    }
}
